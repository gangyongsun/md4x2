package cn.com.goldwind.md4x.business.bo.zeppelin;

public class ZeppelinParagraph {
	private String title;
	private String text;
	private ZeppelinParagraphConfig config;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public ZeppelinParagraphConfig getConfig() {
		return config;
	}

	public void setConfig(ZeppelinParagraphConfig config) {
		this.config = config;
	}

}
